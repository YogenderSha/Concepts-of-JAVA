package infojava;

public class Class3_Typecasting {

	public static void main(String[] args) {
		  //Implicit Typecasting
		short x =  10;  
        int y = x;  
        System.out.println(y);  
      
        //Explicit Typecasting
        double a =  10.0123456789d;  
        float b = (float)a;  
        System.out.println(b);  
        
	}

}
